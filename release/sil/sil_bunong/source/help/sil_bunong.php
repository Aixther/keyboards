<h1>Start Using Bunong ឞូន៝ង (SIL) Keyboard</h1>

<p style="text-align: justify;">
កាដារនទើ​ល៝ត់អ្យា ញជេងគៃមាញចីសរាឆ៝តានាវងើយឞូន៝ង អីទ៝ង់រាឆ៝ទ្រោន។
</p>
<p style="text-align: justify;">
This keyboard is designed for typing Bunong language using Khmer characters.</p>

<p style="text-align: justify;">
នតីលញចីស «Khmer Mondulkiri» គែសលាប់តាត្រោម កាដារនទើល៝ត់​ «Bunong ឞូន៝ង (SIL)»។  នោកឞ៝ន់ងលាប់ មោលាសរាហ្វាត់ កាដារនទើល៝ត់​ «Bunong ឞូន៝ង (SIL)»  គែសងលាប់​ មោលាសគែសរាហ្វាត់ នតីលញចីស «Khmer Mondulkiri» នទ្រែលដ៝ង់។ 
</p>
<p style="text-align: justify;">
Khmer Mondulkiri font is included in the keyboard package. It will be installed and uninstalled along with the keyboard.
</p>

<p style="text-align: justify;">
ទឹងលែៈរាង្លៃរាង្លាប់តាត្រោមតោរាឆ៝ប់គែសញ៝ម់នអឹរ គៃមាកើលអ៝ន់ឞ៝ន់ទើសមាឆៃរាង្លៃរាង្លាប់។
</p>
<p style="text-align: justify;">
All vowels on the phone layout are highlighted to help you locate where the Bunong vowels are.
</p>

